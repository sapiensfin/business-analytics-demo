import streamlit as st
import pandas as pd
import plotly.express as px
import plotly.graph_objects as go

st.set_page_config(page_title="Business Architect: 12-Month Financial Model", layout="wide")

# --- 1. –ì–ï–ù–ï–†–ê–¶–Ü–Ø –†–ï–ê–õ–Ü–°–¢–ò–ß–ù–ò–• –î–ê–ù–ò–• (12 –ú–Ü–°–Ø–¶–Ü–í) ---
def get_extended_data():
    data = []
    months = [f"2025-{m:02d}" for m in range(1, 13)]
    
    # –°—Ç–∞—Ç—Ç—ñ –≤–∏—Ç—Ä–∞—Ç –¥–ª—è –ª–æ–≥—ñ—Å—Ç–∏–∫–∏ —Ç–∞ —Ä–∏—Ç–µ–π–ª—É [cite: 7]
    expense_categories = {
        '–û—Ä–µ–Ω–¥–∞ —Ç–∞ —Å–∫–ª–∞–¥': 55000,
        '–ó–∞—Ä–ø–ª–∞—Ç–∞ –æ—Ñ—ñ—Å': 65000,
        '–ó–∞—Ä–ø–ª–∞—Ç–∞ –≤–æ–¥—ñ—ó': 180000,
        '–ë–æ–Ω—É—Å–∏ –∑–∞ KPI': 25000,
        '–ü–ú–ú (–ü–∞–ª–∏–≤–æ)': 95000,
        '–õ—ñ–∑–∏–Ω–≥ –∞–≤—Ç–æ': 70000,
        '–°—Ç—Ä–∞—Ö—É–≤–∞–Ω–Ω—è (OC/AC)': 12000,
        '–†–µ–º–æ–Ω—Ç —Ç–∞ —Å–µ—Ä–≤—ñ—Å': 20000,
        '–ú–∞—Ä–∫–µ—Ç–∏–Ω–≥ —Ç–∞ —Ä–µ–∫–ª–∞–º–∞': 15000,
        '–ö–æ–º—É–Ω–∞–ª—å–Ω—ñ (Media)': 8000,
        '–ü–æ—Å–ª—É–≥–∏ –±—É—Ö–≥–∞–ª—Ç–µ—Ä—ñ—ó': 6000,
        '–ü–æ–¥–∞—Ç–∫–∏ –Ω–∞ –§–û–ü/–ó–ü': 55000,
        '–ê–º–æ—Ä—Ç–∏–∑–∞—Ü—ñ—è': 10000
    }

    for m in months:
        # –î–æ—Ö–æ–¥–∏ –∑ –Ω–µ–≤–µ–ª–∏–∫–∏–º —Å–µ–∑–æ–Ω–Ω–∏–º –∑—Ä–æ—Å—Ç–∞–Ω–Ω—è–º
        month_num = int(m.split('-')[1])
        seasonality = 1 + (month_num * 0.02)
        data.append({'–î–∞—Ç–∞': f'{m}-05', '–¢–∏–ø': '1. –ü–†–ò–•–û–î–ò', '–°—Ç–∞—Ç—Ç—è': '–í–∏—Ä—É—á–∫–∞ (–†–∞—Ö—É–Ω–æ–∫)', '–°—É–º–∞': 550000 * seasonality})
        data.append({'–î–∞—Ç–∞': f'{m}-07', '–¢–∏–ø': '1. –ü–†–ò–•–û–î–ò', '–°—Ç–∞—Ç—Ç—è': '–í–∏—Ä—É—á–∫–∞ (–ì–æ—Ç—ñ–≤–∫–∞)', '–°—É–º–∞': 120000 * seasonality})
        
        # –í–∏—Ç—Ä–∞—Ç–∏
        for cat, amt in expense_categories.items():
            # –ú–æ–¥–µ–ª—é—î–º–æ –ø—ñ–∫ –≤–∏—Ç—Ä–∞—Ç –Ω–∞ —Ä–µ–º–æ–Ω—Ç —É –±–µ—Ä–µ–∑–Ω—ñ —Ç–∞ –∂–æ–≤—Ç–Ω—ñ
            current_amt = amt
            if cat == '–†–µ–º–æ–Ω—Ç —Ç–∞ —Å–µ—Ä–≤—ñ—Å' and month_num in [3, 10]:
                current_amt *= 4
            if cat == '–ü–ú–ú (–ü–∞–ª–∏–≤–æ)' and month_num > 6:
                current_amt *= 1.15 # –∑—Ä–æ—Å—Ç–∞–Ω–Ω—è —Ü—ñ–Ω –≤–ª—ñ—Ç–∫—É
                
            data.append({'–î–∞—Ç–∞': f'{m}-15', '–¢–∏–ø': '2. –í–ò–¢–†–ê–¢–ò', '–°—Ç–∞—Ç—Ç—è': cat, '–°—É–º–∞': current_amt})
            
    return pd.DataFrame(data)

# --- 2. WHAT-IF –ü–ê–†–ê–ú–ï–¢–†–ò ---
st.sidebar.header("üïπÔ∏è –°–∏–º—É–ª—è—Ç–æ—Ä —Ä—ñ—à–µ–Ω—å")
inc_change = st.sidebar.slider("–ó–º—ñ–Ω–∞ –¥–æ—Ö–æ–¥—É (%),", -20, 40, 0)
exp_opt = st.sidebar.slider("–û–ø—Ç–∏–º—ñ–∑–∞—Ü—ñ—è –≤–∏—Ç—Ä–∞—Ç (%),", -30, 0, 0)
init_bal = st.sidebar.number_input("–ü–æ—á–∞—Ç–∫–æ–≤–∏–π –∫–∞–ø—ñ—Ç–∞–ª (PLN)", value=150000)

# --- 3. –û–ë–†–û–ë–ö–ê –î–ê–ù–ò–• ---
df = get_extended_data()
df['–°—É–º–∞'] = pd.to_numeric(df['–°—É–º–∞'])
df.loc[df['–¢–∏–ø'] == '1. –ü–†–ò–•–û–î–ò', '–°—É–º–∞'] *= (1 + inc_change / 100)
df.loc[df['–¢–∏–ø'] == '2. –í–ò–¢–†–ê–¢–ò', '–°—É–º–∞'] *= (1 + exp_opt / 100)

df['–î–∞—Ç–∞'] = pd.to_datetime(df['–î–∞—Ç–∞'])
df['–ú—ñ—Å—è—Ü—å'] = df['–î–∞—Ç–∞'].dt.strftime('%m-%Y')

# --- 4. –ì–û–õ–û–í–ù–ò–ô –ó–í–Ü–¢ P&L ---
st.title("üìä –†—ñ—á–Ω–∞ —Ñ—ñ–Ω–∞–Ω—Å–æ–≤–∞ –º–æ–¥–µ–ª—å –±—ñ–∑–Ω–µ—Å—É")
st.markdown(f"**–ï–∫—Å–ø–µ—Ä—Ç–Ω–∏–π –∞–Ω–∞–ª—ñ–∑: –í—ñ—Ç–∞–ª—ñ–π –û–º–µ—Ü–∏–Ω—Å—å–∫–∏–π** [cite: 1]")

pnl = df.pivot_table(index=['–¢–∏–ø', '–°—Ç–∞—Ç—Ç—è'], columns='–ú—ñ—Å—è—Ü—å', values='–°—É–º–∞', aggfunc='sum', sort=False)
st.subheader("üìë –†–æ–∑–≥–æ—Ä–Ω—É—Ç–∏–π –∑–≤—ñ—Ç P&L (12 –º—ñ—Å—è—Ü—ñ–≤)")
st.dataframe(pnl.style.format("{:,.0f}").background_gradient(cmap='RdYlGn', axis=1), use_container_width=True)

# --- 5. –†–û–ó–†–ê–•–£–ù–û–ö –†–ï–ù–¢–ê–ë–ï–õ–¨–ù–û–°–¢–Ü ---
st.divider()
st.subheader("üí° –ü–æ–∫–∞–∑–Ω–∏–∫–∏ –µ—Ñ–µ–∫—Ç–∏–≤–Ω–æ—Å—Ç—ñ (Efficiency Metrics)")

income = df[df['–¢–∏–ø'] == '1. –ü–†–ò–•–û–î–ò'].groupby('–ú—ñ—Å—è—Ü—å', sort=False)['–°—É–º–∞'].sum()
expense = df[df['–¢–∏–ø'] == '2. –í–ò–¢–†–ê–¢–ò'].groupby('–ú—ñ—Å—è—Ü—å', sort=False)['–°—É–º–∞'].sum()
profit = income - expense
# EBITDA (—Å–ø—Ä–æ—â–µ–Ω–æ: –ø—Ä–∏–±—É—Ç–æ–∫ + –∞–º–æ—Ä—Ç–∏–∑–∞—Ü—ñ—è)
ebitda = profit + df[df['–°—Ç–∞—Ç—Ç—è'] == '–ê–º–æ—Ä—Ç–∏–∑–∞—Ü—ñ—è'].groupby('–ú—ñ—Å—è—Ü—å', sort=False)['–°—É–º–∞'].sum()
margin = (profit / income * 100)

c1, c2, c3, c4 = st.columns(4)
c1.metric("–†—ñ—á–Ω–∞ –≤–∏—Ä—É—á–∫–∞", f"{income.sum():,.0f} PLN")
c2.metric("–ß–∏—Å—Ç–∏–π –ø—Ä–∏–±—É—Ç–æ–∫", f"{profit.sum():,.0f} PLN")
c3.metric("–°–µ—Ä–µ–¥–Ω—è —Ä–µ–Ω—Ç–∞–±–µ–ª—å–Ω—ñ—Å—Ç—å", f"{margin.mean():.1f} %")
c4.metric("EBITDA (–†—ñ—á–Ω–∞)", f"{ebitda.sum():,.0f} PLN")

# --- 6. –ì–†–ê–§–Ü–ö CASH FLOW ---
st.subheader("üìâ –ü—Ä–æ–≥–Ω–æ–∑ –∫–∞—Å–æ–≤–∏—Ö —Ä–æ–∑—Ä–∏–≤—ñ–≤ (12 –º—ñ—Å—è—Ü—ñ–≤)")
df = df.sort_values('–î–∞—Ç–∞')
df['–ó–º—ñ–Ω–∞'] = df.apply(lambda x: x['–°—É–º–∞'] if '–ü–†–ò–•–û–î–ò' in x['Typ'] else -x['–°—É–º–∞'], axis=1) # –¢–∏–ø–æ –≤–∏–ø—Ä–∞–≤–ª–µ–Ω–æ –Ω–∏–∂—á–µ
# –í–∏–ø—Ä–∞–≤–ª–µ–Ω–Ω—è –¥–ª—è —Å—Ç–∞–±—ñ–ª—å–Ω–æ—Å—Ç—ñ:
df['–ó–º—ñ–Ω–∞'] = df.apply(lambda x: x['–°—É–º–∞'] if '–ü–†–ò–•–û–î–ò' in x['–¢–∏–ø'] else -x['–°—É–º–∞'], axis=1)
df['–ó–∞–ª–∏—à–æ–∫'] = init_bal + df['–ó–º—ñ–Ω–∞'].cumsum()

fig = go.Figure()
fig.add_trace(go.Scatter(x=df['–î–∞—Ç–∞'], y=df['–ó–∞–ª–∏—à–æ–∫'], mode='lines', fill='tozeroy', name='–ë–∞–ª–∞–Ω—Å', line=dict(color='#00CC96', width=3)))
fig.add_hline(y=0, line_dash="dash", line_color="red")
st.plotly_chart(fig, use_container_width=True)

if df['–ó–∞–ª–∏—à–æ–∫'].min() < 0:
    st.error(f"‚ö†Ô∏è –í–∏—è–≤–ª–µ–Ω–æ —Ä–∏–∑–∏–∫ –∫–∞—Å–æ–≤–æ–≥–æ —Ä–æ–∑—Ä–∏–≤—É! –ú—ñ–Ω—ñ–º–∞–ª—å–Ω–∏–π –±–∞–ª–∞–Ω—Å: {df['–ó–∞–ª–∏—à–æ–∫'].min():,.0f} PLN")
